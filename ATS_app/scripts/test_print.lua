    ats.log("=== 打印机测试开始 ===")
    
    -- 检查打印纸状态
    local has_paper = ats.printer_get_paper_status()
    ats.log("打印纸状态: " .. (has_paper and "有纸" or "无纸"))
    
    -- 打开打印机
    local ret = ats.printer_open()
    ats.assert_true(ret == 0, "打印机打开失败")
    ats.log("打印机已打开")
    
    -- 设置有纸状态
    ats.printer_set_paper_status(true)
    
    -- ========== 打印 Logo（位图占位）==========
