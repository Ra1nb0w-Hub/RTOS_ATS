    ats.printer_set_font_size(FONT_DOUBLE_HEIGHT)
    ats.printer_print_data("POS Transfer", true)
    
    -- 金额（两端对齐，双倍高）
    ats.printer_set_align(ALIGN_LEFT)
    print_align_both_end("Amount", "NGN 10,531.00", FONT_DOUBLE_HEIGHT, true)
    
    -- 分隔线
    print_divider('-')
    
    -- ========== 交易结果（居中，双倍高）==========
    ats.printer_set_align(ALIGN_CENTER)
    ats.printer_set_font_size(FONT_DOUBLE_HEIGHT)
    ats.printer_print_data("TRANSACTION APPROVED", true)
    
    -- ========== 交易详情（两端对齐）==========
    ats.printer_set_align(ALIGN_LEFT)
    print_align_both_end("Date/Time", "24/03/2026 11:22", FONT_NORMAL, true)
