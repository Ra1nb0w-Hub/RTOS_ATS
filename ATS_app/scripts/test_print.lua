    ats.printer_set_align(ALIGN_CENTER)
    ats.printer_set_font_size(FONT_NORMAL)
    ats.printer_print_bitmap(LOGO_BITMAP, LOGO_BITMAP_WIDTH, LOGO_BITMAP_HEIGHT);
    print_empty_line(1)
    
    -- ========== 商户信息（两端对齐）==========
    ats.printer_set_align(ALIGN_LEFT)
    print_align_both_end("MCH Name", "2TRANQUILITY RAY GLOBAL COMMUNICATIONS LIMITED", FONT_NORMAL, true)
    print_align_both_end("MCH Addr", "test|address", FONT_NORMAL, true)
