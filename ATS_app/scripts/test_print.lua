local LOGO_BITMAP_WIDTH = 236
local LOGO_BITMAP_HEIGHT = 80

--[[
 * @brief 打印两端对齐的键值对（模拟 OPayPrinterSetAlignBothEndData）
 * @param key 左侧文本
 * @param value 右侧文本
 * @param font_size 字体大小
 * @param end_line 是否换行
--]]
local function print_align_both_end(key, value, font_size, end_line)
    font_size = font_size or FONT_NORMAL
    end_line = end_line or true
    
    -- 计算可用宽度（58mm纸约32个正常字符）
    local max_chars = 32
    if font_size == FONT_DOUBLE_WIDTH or font_size == FONT_DOUBLE_WIDTH_HEIGHT then
        max_chars = 16
    end
    
    -- 计算填充空格数
    local total_len = string.len(key) + string.len(value)
    local padding = max_chars - total_len
    if padding < 1 then
        padding = 1
    end
    
    -- 组合文本：key + 空格 + value
    local text = key .. string.rep(" ", padding) .. value
    
    ats.printer_set_font_size(font_size)
    ats.printer_print_data(text, end_line)
end

--[[
 * @brief 打印分隔线
 * @param char 分隔字符，默认 '-'
--]]
local function print_divider(char)
    char = char or '-'
    ats.printer_set_font_size(FONT_NORMAL)
    ats.printer_print_data(string.rep(char, 32), true)
end

--[[
 * @brief 打印空行
 * @param count 空行数量
--]]
local function print_empty_line(count)
    count = count or 1
    for i = 1, count do
        ats.printer_print_data("", true)
    end
end

--[[
 * @brief 打印联次信息（客户联/商户联）
 * @param copy_type 联次类型：0=客户联, 1=商户联
--]]
local function print_ticket_copy(copy_type)
    ats.printer_set_align(ALIGN_CENTER)
    ats.printer_set_font_size(FONT_NORMAL)
    
    local copy_text = ""
    if copy_type == 0 then
        copy_text = "-------- CUSTOMER COPY ---------"
    else
        copy_text = "-------- MERCHANT COPY -------- "
    end

    ats.printer_print_data(copy_text, true)
    print_empty_line(1)
end

