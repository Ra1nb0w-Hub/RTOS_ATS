/** 扩展小票缓冲区高度（如果需要更多空间） */
static int ensure_buffer_height(int needed_y)
{
    if (needed_y <= s_receipt_height)
        return 0;

    int new_height = s_receipt_height * 2;
    if (new_height < needed_y)
        new_height = needed_y + 256;

    unsigned char *new_buf = (unsigned char *)malloc(s_receipt_width * new_height);
    if (!new_buf)
        return -1;

    /* 新缓冲区初始化为全白 */
    memset(new_buf, 0xFF, s_receipt_width * new_height);

    /* 拷贝旧数据 */
    if (s_receipt_buf && s_receipt_height > 0)
    {
        memcpy(new_buf, s_receipt_buf, s_receipt_width * s_receipt_height);
        free(s_receipt_buf);
    }

    s_receipt_buf   = new_buf;
    s_receipt_height = new_height;
    return 0;
}

/**
 * @brief 将 12x24 字模渲染到缓冲区
 *
 * 从 font_12x24 数组中读取字模数据（大段序列，每行 16 字节，MSB 在左），
 * 根据字体大小模式进行 2 倍缩放后渲染。
 */
static void draw_char_12x24(unsigned char ch, int x, int y)
{
    int char_idx = -1;

    if (ch >= 0x20 && ch <= 0x7E)
    {
        char_idx = (ch - 0x20) * FONT_BYTES_PER_CHAR;
    }
    else
    {
        /* 不可打印字符：不渲染 */
        return;
    }

    /* 检查是否为空格（全 0 数据） */
    int is_space = 1;
    for (int i = 0; i < FONT_BYTES_PER_CHAR; i++)
    {
        if (font_12x24[char_idx + i] != 0)
        {
            is_space = 0;
            break;
        }
    }
    if (is_space)
        return;

    int scale_w = 1, scale_h = 1;

    /* 根据字体大小确定缩放 */
    switch (s_font_size)
    {
    case ATS_PRINTER_FONT_SIZE_DOUBLE_WIDTH:
        scale_w = 2; scale_h = 1; break;
    case ATS_PRINTER_FONT_SIZE_DOUBLE_HEIGHT:
        scale_w = 1; scale_h = 2; break;
    case ATS_PRINTER_FONT_SIZE_DOUBLE_WIDTH_HEIGHT:
        scale_w = 2; scale_h = 2; break;
    default:
        scale_w = 1; scale_h = 1; break;
    }

    int render_h = FONT_CHAR_H * scale_h;

    /* 确保缓冲区够高 */
    if (ensure_buffer_height(y + render_h) < 0)
        return;

    /* 逐行逐列渲染字模 */
    for (int row = 0; row < FONT_CHAR_H; row++)
    {
        int row_offset = char_idx + row * FONT_ROW_BYTES;

        for (int col = 0; col < FONT_CHAR_W; col++)
        {
            /* 大段序列：MSB 在左，每字节 8 像素 */
            int byte_idx = col / 8;
            int bit_pos  = 7 - (col % 8);
            int bit = (font_12x24[row_offset + byte_idx] >> bit_pos) & 0x01;

            if (!bit)
                continue;  /* bit=0 → 无墨点，跳过 */

            /* 写入像素（放大） */
            for (int sy = 0; sy < scale_h; sy++)
            {
                for (int sx = 0; sx < scale_w; sx++)
                {
                    int px = x + col * scale_w + sx;
                    int py = y + row * scale_h + sy;

                    if (px >= 0 && px < s_receipt_width &&
                        py >= 0 && py < s_receipt_height)
                    {
                        /* bit=1 → 墨点 → 黑色 */
                        s_receipt_buf[py * s_receipt_width + px] = 0x00;
                    }
                }
            }
        }
    }
}

/** 计算当前字体大小下每行最大字符数 */
static int get_line_max_chars(void)
{
    int max_chars = FONT_LINE_MAX_CHARS;
    switch (s_font_size)
    {
    case ATS_PRINTER_FONT_SIZE_DOUBLE_WIDTH:
    case ATS_PRINTER_FONT_SIZE_DOUBLE_WIDTH_HEIGHT:
        max_chars = FONT_LINE_MAX_CHARS / 2;
        break;
    default:
        break;
    }
    return max_chars;
}

/** 计算当前字体大小下单个字符占用的像素宽度 */
static int get_char_pixel_width(void)
{
    switch (s_font_size)
    {
    case ATS_PRINTER_FONT_SIZE_DOUBLE_WIDTH:
    case ATS_PRINTER_FONT_SIZE_DOUBLE_WIDTH_HEIGHT:
        return FONT_CHAR_W * 2;
    default:
        return FONT_CHAR_W;
    }
}

/** 计算当前字体大小下行高 */
static int get_line_pixel_height(void)
{
    switch (s_font_size)
    {
    case ATS_PRINTER_FONT_SIZE_DOUBLE_HEIGHT:
    case ATS_PRINTER_FONT_SIZE_DOUBLE_WIDTH_HEIGHT:
        return FONT_CHAR_H * 2;
    default:
        return FONT_CHAR_H;
    }
}

/**
 * @brief 渲染一行文本到小票缓冲区
 */
static void render_text_line(const char *text)
{
    int len = (int)strlen(text);
    if (len == 0)
    {
        /* 空行：直接跳过当前行高度 */
        s_cursor_y += get_line_pixel_height();
        return;
    }

    int char_w = get_char_pixel_width();
    int line_h = get_line_pixel_height();
    int max_chars = get_line_max_chars();

    /* 截断过长文本 */
    if (len > max_chars)
        len = max_chars;

    int text_pixels_w = len * char_w;

    /* 计算对齐起始 X */
    int start_x = RECEIPT_MARGIN; /* 默认左对齐 */

    switch (s_align_mode)
    {
    case ATS_PRINTER_ALIGN_MODE_CENTER:
        start_x = (s_receipt_width - text_pixels_w) / 2;
        if (start_x < RECEIPT_MARGIN) start_x = RECEIPT_MARGIN;
        break;
    case ATS_PRINTER_ALIGN_MODE_RIGHT:
        start_x = s_receipt_width - text_pixels_w - RECEIPT_MARGIN;
        if (start_x < RECEIPT_MARGIN) start_x = RECEIPT_MARGIN;
        break;
    default:
        break;
    }

    /* 确保缓冲区够高 */
    if (ensure_buffer_height(s_cursor_y + line_h) < 0)
        return;

    /* 逐字符渲染 */
    for (int i = 0; i < len; i++)
    {
        draw_char_12x24((unsigned char)text[i], start_x + i * char_w, s_cursor_y);
    }

    s_cursor_y += line_h;
}

/**
 * @brief 渲染单色位图到小票缓冲区
 *
 * @param data  单色位图数据，每行按字节对齐，MSB 在左
 * @param width 图像宽度（像素）
 * @param height 图像高度（像素）
 */
static void render_bitmap(const unsigned char *data, int width, int height)
{
    if (!data || width <= 0 || height <= 0)
        return;

    /* 确保缓冲区够高 */
    if (ensure_buffer_height(s_cursor_y + height) < 0)
        return;

    /* 计算对齐起始 X */
    int start_x = 16;
    switch (s_align_mode)
    {
    case ATS_PRINTER_ALIGN_MODE_CENTER:
        start_x = (s_receipt_width - width) / 2;
        if (start_x < 16) start_x = 16;
        break;
    case ATS_PRINTER_ALIGN_MODE_RIGHT:
        start_x = s_receipt_width - width - 16;
        if (start_x < 16) start_x = 16;
        break;
    default:
        break;
    }

    int bytes_per_row = (width + 7) / 8;

    for (int row = 0; row < height; row++)
    {
        for (int col = 0; col < width; col++)
        {
            int byte_idx = row * bytes_per_row + col / 8;
            int bit_idx  = 7 - (col % 8);
            int pixel = (data[byte_idx] >> bit_idx) & 0x01;

            int px = start_x + col;
            int py = s_cursor_y + row;

            if (px >= 0 && px < s_receipt_width &&
                py >= 0 && py < s_receipt_height)
            {
                /* bit=1 → 黑色墨点，bit=0 → 不画（保留白色背景） */
                if (pixel)
                    s_receipt_buf[py * s_receipt_width + px] = 0x00;
            }
        }
    }

    s_cursor_y += height;
}

