static void render_text_data_impl(char *data, bool is_end_of_line)
{
    if (!s_opened || !s_receipt_buf)
        return;

    if (!data)
        return;

    /* 保存当前对齐模式，用于换行后恢复 */
    ats_printer_align_mode_t saved_align = s_align_mode;
    
    /* 空字符串处理：is_end_of_line=true 时换行，否则直接返回 */
    if (data[0] == '\0')
    {
        if (is_end_of_line)
        {
            s_cursor_y += get_line_pixel_height();
            s_align_mode = saved_align;
        }
        return;
    }
    
    /* 处理数据中的换行符和回车符 */
    /* 注意：不修改原字符串（可能是只读内存），使用长度控制 */
    char *start = data;
    char *p = data;
    
    while (*p != '\0')
    {
        if (*p == '\n')
        {
            /* 遇到 \n：处理当前行并换行 */
            /* 临时截断：复制当前段到缓冲区 */
            int len = (int)(p - start);
            if (len > 0)
            {
                /* 处理 \r（当作空格）：先复制到栈缓冲区替换 */
                char line_buf[256];
                if (len >= (int)sizeof(line_buf))
                    len = (int)sizeof(line_buf) - 1;
                memcpy(line_buf, start, len);
                line_buf[len] = '\0';
                /* 将 \r 替换为空格 */
                for (int i = 0; i < len; i++)
                {
                    if (line_buf[i] == '\r')
                        line_buf[i] = ' ';
                }
                render_text_line(line_buf);
            }
            else
            {
                /* 空行：直接跳过当前行高度 */
                s_cursor_y += get_line_pixel_height();
            }
            
            /* 换行后恢复对齐模式 */
            s_align_mode = saved_align;
            start = p + 1;
        }
        p++;
    }
    
    /* 处理剩余数据 */
    if (*start != '\0' || start == data)
    {
        int len = (int)strlen(start);
        if (len > 0 || is_end_of_line)
        {
            /* 复制到栈缓冲区处理 \r */
            char line_buf[256];
            if (len >= (int)sizeof(line_buf))
                len = (int)sizeof(line_buf) - 1;
            if (len > 0)
                memcpy(line_buf, start, len);
            line_buf[len] = '\0';
            /* 将 \r 替换为空格 */
            for (int i = 0; i < len; i++)
            {
                if (line_buf[i] == '\r')
                    line_buf[i] = ' ';
            }
            
            if (is_end_of_line)
            {
                /* is_end_of_line = true：渲染并强制换行 */
                if (len > 0)
                    render_text_line(line_buf);
                else
                    s_cursor_y += get_line_pixel_height();
                /* 换行后恢复对齐模式 */
                s_align_mode = saved_align;
            }
            else
            {
                /* is_end_of_line = false：只渲染不换行 */
                if (len > 0)
                    render_text_line(line_buf);
            }
        }
    }
}

int ats_printer_set_print_data(char *data, bool is_end_of_line)
{
    if (!s_opened)
        return -1;
    if (!data)
        return 0;
    if (s_pending_count >= ATS_PRINTER_MAX_PENDING_OPS)
        return -1;

    printer_op_t *op = &s_pending_ops[s_pending_count++];
    op->type = PRINTER_OP_TEXT;
    op->align = s_align_mode;
    op->font = s_font_size;
    op->is_end_of_line = is_end_of_line;
    strncpy(op->text, data, sizeof(op->text) - 1);
    op->text[sizeof(op->text) - 1] = '\0';
    op->bitmap_data = NULL;
    return 0;
}

static void render_bitmap_impl(const unsigned char *data, int width, int height)
{
    if (!s_opened || !s_receipt_buf)
        return;

    if (!data || width <= 0 || height <= 0)
        return;

    render_bitmap(data, width, height);
}

int ats_printer_set_print_bitmap(unsigned char *data, int width, int height)
{
    if (!s_opened)
        return -1;
    if (!data || width <= 0 || height <= 0)
        return -1;
    if (s_pending_count >= ATS_PRINTER_MAX_PENDING_OPS)
        return -1;

    int bytes = ((width + 7) / 8) * height;
    unsigned char *copy = (unsigned char *)malloc(bytes);
    if (!copy)
        return -1;
    memcpy(copy, data, bytes);

    printer_op_t *op = &s_pending_ops[s_pending_count++];
    op->type = PRINTER_OP_BITMAP;
    op->align = s_align_mode;
    op->font = ATS_PRINTER_FONT_SIZE_NORMAL;
    op->is_end_of_line = false;
    op->text[0] = '\0';
    op->bitmap_data = copy;
    op->bitmap_w = width;
    op->bitmap_h = height;
    return 0;
}

